export const useI18nStore = defineStore('i18n', {
    state: () => ({
        locale: localStorage.getItem('locale') || 'es',
    }),

    getters: {
        t: (state) => (key) => {
            return translations[state.locale]?.[key] || translations.es[key] || key;
        },
        tp: (state) => (key, params = {}) => {
            let str = translations[state.locale]?.[key] || translations.es[key] || key;
            Object.entries(params).forEach(([k, v]) => {
                str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
            });
            return str;
        },
        isSpanish: (state) => state.locale === 'es',
        isEnglish: (state) => state.locale === 'en',
    },

    actions: {
        async setLocale(lang) {
            if (translations[lang]) {
                this.locale = lang;
                localStorage.setItem('locale', lang);
                try { await api.post('/configuracion/idioma', { idioma: lang }); } catch(e) {}
            }
        },
        toggle() {
            this.setLocale(this.locale === 'es' ? 'en' : 'es');
        },
        loadFromServer(idioma) {
            if (idioma && translations[idioma]) {
                this.locale = idioma;
                localStorage.setItem('locale', idioma);
            }
        },
    },
});
